    private DefaultListModel getComicIsbns() {
        ArrayList<String> comicIsbns = new ArrayList<String>();
        for (int i = 0; i < comics.length; i++) {
            comicIsbns.add(comics[i].getIsbn());
        }
        model = new DefaultListModel();
        list = new JList(model);
        for (int j = 0; j < comics.length; j++) {
            model.addElement(comics[j].getIsbn());
        }
        return model;
    }

