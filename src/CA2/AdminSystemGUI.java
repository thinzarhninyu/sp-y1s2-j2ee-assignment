    private void btnAddComicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddComicActionPerformed
        // TODO add your handling code here:
        String message = "";
        // if no input for pages, set to 0
        if (txtPages.getText().equals("")) {
            txtPages.setText("0");
        }
        // if no input for cost, set to 0
        if (txtCost.getText().equals("")) {
            txtCost.setText("0");
        }
        // check for input for comic type
        if (!rbComic.isSelected() && !rbMangaJP.isSelected() && !rbMangaEN.isSelected()) {
            message = "Please select the type of comic!";
        } else {
            if (rbComic.isSelected()) {
                message = rs.addComic(txtTitle.getText(), txtIsbn.getText(), Integer.parseInt(txtPages.getText()), Double.parseDouble(txtCost.getText()));
            } else if (rbMangaEN.isSelected()) {
                message = rs.addManga(txtTitle.getText(), txtIsbn.getText(), Integer.parseInt(txtPages.getText()), Double.parseDouble(txtCost.getText()), "EN");
            } else {
                message = rs.addManga(txtTitle.getText(), txtIsbn.getText(), Integer.parseInt(txtPages.getText()), Double.parseDouble(txtCost.getText()), "JP");
            }
        }
        // set text to display
        txtMessage.setText(message);
        this.dispose();
        AdminSystemGUI asg = new AdminSystemGUI();
        asg.setVisible(true);
    }//GEN-LAST:event_btnAddComicActionPerformed

