    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        int index = 0;
        String text = "";
        int comicIndex = 0;
        // if search rentee
        if (rbDeleteRentee.isSelected()) {
            index = rs.searchMember(txtID.getText());
            // if member cannot be found
            if (index == -1) {
                text = "No Such Member ID \"" + txtID.getText() + "\" in the data!";
            } 
            // if member is found
            else {
                text += "Member ID: " + rs.getRentees()[index].getMemberID() + "\nName: " + rs.getRentees()[index].getName() + "\nComics Borrowed: ";
                for (int i = 0; i < rs.getRentees()[index].getComics().length; i++) {
                    comicIndex = rs.searchComic(rs.getRentees()[index].getComics()[i]);
                    // get comics by rentee
                    if (comicIndex != -1) {
                        text += "\n" + (i + 1) + ": " + rs.getComics()[comicIndex].getTitle();
                    }
                }
            }
        } 
        // if search comic
        else {
            index = rs.searchComic(txtID.getText());
            // if comic cannot be found
            if (index == -1) {
                text = "No Such ISBN-13 \"" + txtID.getText() + "\" in the data!";
            }
            // if comic is found
            else {
                text += "ISBN-13: " + rs.getComics()[index].getIsbn() + "\nName: " + rs.getComics()[index].getTitle() + "\nRental Rate: " + rs.getComics()[index].calculateDayPrice() + "\nDeposit: " + rs.getComics()[index].calculateDepositFee() + "\n" + rs.getComics()[index].getLanguage();
            }
        }
        // set text to display
        txtMessage.setText(text);
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = 0;
        String text = "";
        // if delete rentee
        if (rbDeleteRentee.isSelected()) {
            index = rs.deleteRentee(txtID.getText());
            // if rentee cannot be found
            if (index == -1) {
                text = "No Such Member ID \"" + txtID.getText() + "\" in the data!";
            } 
            // if process is cancelled by user
            else if (index == -2) {
                text = "Cancelled!";
            } 
            // rentee removed
            else {
                text = "Rentee removed!";
            }
        } 
        // if delete comic
        else {
            index = rs.deleteComic(txtID.getText());
            // if comic cannot be found
            if (index == -1) {
                text = "No Such ISBN-13 \"" + txtID.getText() + "\" in the data!";
            } 
            // if process is cancelled by user
            else if (index == -2) {
                text = "Cancelled!";
            } 
            // comic removed
            else {
                text = "Comic removed!";
            }
        }
        // text to display
        txtMessage.setText(text);
        this.dispose();
        AdminSystemGUI asg = new AdminSystemGUI();
        asg.setVisible(true);
    }//GEN-LAST:event_btnDeleteActionPerformed

